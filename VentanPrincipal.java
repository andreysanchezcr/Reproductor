    private void volumenStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_volumenStateChanged
            try { 
            miReproductor.control.setGain((double)volumen.getValue()/100);
            
        } catch (BasicPlayerException ex) {
            Logger.getLogger(VentanPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }    
            vol.setText(volumen.getValue()+"%");
    }//GEN-LAST:event_volumenStateChanged

