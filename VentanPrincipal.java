    private void btnPlayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlayActionPerformed
        switch (btnPlay.getText()) { 
            case "Play": 
                if (!enReproduccion&&archivo!=null) { 
                    
                    try { 
                        miReproductor.control.play(); 
                        btnPlay.setText("Pausa"); 
                        enReproduccion = true; 
                        reproduciendo.setText("Abriendo: "+archivo.getName());
                    } catch (BasicPlayerException ex) { 
                        Logger.getLogger(VentanPrincipal.class.getName()).log(Level.SEVERE, null, ex); 
                    } 
                } 
                 if (enReproduccion) { 
            try { 
                miReproductor.control.resume(); 
                btnPlay.setText("Pausa"); 
            } catch (BasicPlayerException ex) { 
                Logger.getLogger(VentanPrincipal.class.getName()).log(Level.SEVERE, null, ex); 
            } 
                        } 
                break; 
            case "Pausa": 
                try { 
                    miReproductor.control.pause(); 
                } catch (BasicPlayerException ex) { 
                    Logger.getLogger(VentanPrincipal.class.getName()).log(Level.SEVERE, null, ex); 
                } 
                btnPlay.setText("Play"); 
                break; 

        }
    }//GEN-LAST:event_btnPlayActionPerformed

    private void btnStopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStopActionPerformed
       try { 
            miReproductor.control.stop(); 
        } catch (BasicPlayerException ex) { 
            Logger.getLogger(VentanPrincipal.class.getName()).log(Level.SEVERE, null, ex); 
        } 
        enReproduccion = false; 
        btnPlay.setText("Play"); 
                            
    }//GEN-LAST:event_btnStopActionPerformed

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archiv"
                + "o MP3", "mp3", "mp3"); 
        archivoSeleccionado.setFileFilter(filtro); 

        int seleccion = archivoSeleccionado.showOpenDialog(this); 

        if (seleccion == JFileChooser.APPROVE_OPTION) { 
            archivo = archivoSeleccionado.getSelectedFile(); 
            try { 
                miReproductor.control.open(archivo);//Le decimos al control del player que abra el archivo 
            } catch (BasicPlayerException ex) { 
                Logger.getLogger(VentanPrincipal.class.getName()).log(Level.SEVERE, null, ex); 
            } 
        } 
    }//GEN-LAST:event_jMenuItem1ActionPerformed

